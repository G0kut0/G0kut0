### Operadores Aritméticos ###

# Operaciones con enteros 
print(3 + 4)    # Suma
print(3 - 4)    # Resta
print(3 * 4)    # Multiplicación
print(3 / 4)    # División
print(5 // 4)   # División entera
print(5 % 4)    # Resto de la División entera
print(3 ** 2)   # Potencia
print(2 + 3 * 4 ** 5 / 7 // 8)
# Poibilidad de hacer operaciones convinadas 
# !NO TIENE EN CUENTA LA JERARQUÍA DE LAS OPERACIONES, RESUELVE DE IZQ A DER¡

print('Hola' + 'Python')    # Suma Strings
print('Hola ' + str(5))     # Sumamos un int a un str
print('Hola ' * 5)
print('Hola ' * int(10 / 3))

